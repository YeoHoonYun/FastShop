package com.fastcompus.FastBlog.vo;

public class ComDetailVO {
	private int companysId;
	private String desc;
	private String comDetail;
	public int getCompanysId() {
		return companysId;
	}
	public void setCompanysId(int companysId) {
		this.companysId = companysId;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getComDetail() {
		return comDetail;
	}
	public void setComDetail(String comDetail) {
		this.comDetail = comDetail;
	}
}
